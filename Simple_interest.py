# Define the principal amount in rupees
principal_amount = 200

# Define the time period in years
time_period = 5

# Define the annual interest rate in percentage
interest_rate = 5

# Calculate simple interest using the formula (P * T * R) / 100
simple_interest = (principal_amount * time_period * interest_rate) / 100

# Display the calculated simple interest
print("Simple Interest is:", simple_interest)

# Calculate and display the total amount after adding simple interest to the principal
total_amount = simple_interest + principal_amount
print("Total Amount after adding Simple Interest is:", total_amount)

